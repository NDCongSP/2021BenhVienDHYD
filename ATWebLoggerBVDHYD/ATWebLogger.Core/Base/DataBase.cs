        /// Converts an instance of an object to a string format
        /// </summary>
        /// <param name="format">Specifies if it should convert to XML, BSON or JSON</param>
        /// <returns>The object, converted to a string representation</returns>
        public string ToString(ATWebLogger.Core.SerializationFormats format)
        {
            SerializableData serializableData = new SerializableData();
            serializableData.Id = IsNull(ATWebLogger.Core.Data.Fields.Id)
                ? (int?) null : _id;
            serializableData.DateTime = IsNull(ATWebLogger.Core.Data.Fields.DateTime)
                ? (DateTime?) null : _dateTime;
            serializableData.LocationId = IsNull(ATWebLogger.Core.Data.Fields.LocationId)
                ? (int?) null : _locationId;
            serializableData.Value = IsNull(ATWebLogger.Core.Data.Fields.Value)
                ? (double?) null : _value;
            serializableData.SerializationIsUpdate = _layerGenIsUpdate;

            if (format == ATWebLogger.Core.SerializationFormats.Json)
            {
                return Newtonsoft.Json.JsonConvert.SerializeObject(serializableData);
            }

            if (format == ATWebLogger.Core.SerializationFormats.Xml)
            {
                System.Xml.Serialization.XmlSerializer xType = new System.Xml.Serialization.XmlSerializer(serializableData.GetType());

                using (StringWriter sw = new StringWriter())
                {
                    xType.Serialize(sw, serializableData);

                    return sw.ToString();
                }
            }

            if (format == ATWebLogger.Core.SerializationFormats.BsonBase64)
            {
                using (System.IO.MemoryStream ms = new System.IO.MemoryStream())
                {
                    using (Newtonsoft.Json.Bson.BsonWriter writer = new Newtonsoft.Json.Bson.BsonWriter(ms))
                    {
                        Newtonsoft.Json.JsonSerializer serializer = new Newtonsoft.Json.JsonSerializer();
                        serializer.Serialize(writer, serializableData);
                    }
                    return Convert.ToBase64String(ms.ToArray());
                }
            }

            return "";
        }

