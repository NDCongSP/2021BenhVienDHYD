        private byte[] GetSerializedObject(DataBase cls)
        {
            byte[] clsid = ObjectToByteArray(cls.Id);
            byte[] clsdateTime = ObjectToByteArray(cls.DateTime);
            byte[] clslocationId = ObjectToByteArray(cls.LocationId);
            byte[] clsvalue = ObjectToByteArray(cls.Value);

            byte[] clsArray = new byte[clsid.Length + clsdateTime.Length + clslocationId.Length + clsvalue.Length];
            Array.Copy(clsid, 0, clsArray, 0, clsid.Length);
            Array.Copy(clsdateTime, 0, clsArray, 0 + clsid.Length, clsdateTime.Length);
            Array.Copy(clslocationId, 0, clsArray, 0 + clsid.Length + clsdateTime.Length, clslocationId.Length);
            Array.Copy(clsvalue, 0, clsArray, 0 + clsid.Length + clsdateTime.Length + clslocationId.Length, clsvalue.Length);

            return clsArray;

        }

        /// <summary>
        /// Determines of the current concurrency array is equal to what's in the database.
        /// </summary>
        /// <returns>True, if the data in memory is equal to whats in the database, otherwise false.</returns>
        protected bool IsDataEqual()
        {
            ATWebLogger.Core.Data obj = new ATWebLogger.Core.Data(Id);
            byte[] myArray = _concurrencyArray;
            byte[] clsArray = GetSerializedObject(obj);

            if (myArray.Length != clsArray.Length)
            {
                return false;
            }

            for (int i = 0; i < myArray.Length; i++)
            {
                if (myArray[i] != clsArray[i])
                {
                    return false;
                }
            }
            return true;
        }

        private byte[] ObjectToByteArray(object obj)
        {
            if (obj == null)
                return new byte[] {};

            if (obj is MySqlGeometry)
            {
                MySqlGeometry obj2 = (MySqlGeometry) obj;
                SimpleGeometry sg = new SimpleGeometry();
                sg.X = obj2.XCoordinate;
                sg.Y = obj2.YCoordinate;
                sg.Srid = obj2.SRID;
                sg.Value = obj2.Value;
                obj = sg;
            }

            BinaryFormatter bf = new BinaryFormatter();
            using (MemoryStream ms = new MemoryStream())
            {
                bf.Serialize(ms, obj);
                return ms.ToArray();
            }
        }

